-- 1. Regions Table
CREATE TABLE RB_INTEL_LEDGER_19C.REGIONS (
    REGION_ID NUMBER PRIMARY KEY,
    REGION_NAME VARCHAR2(100),
    DESCRIPTION VARCHAR2(255)
);

-- 2. Countries Table
CREATE TABLE RB_INTEL_LEDGER_19C.COUNTRIES (
    COUNTRY_ID NUMBER PRIMARY KEY,
    COUNTRY_NAME VARCHAR2(100),
    REGION_ID NUMBER REFERENCES RB_INTEL_LEDGER_19C.REGIONS(REGION_ID)
);

-- 3. Departments Table
CREATE TABLE RB_INTEL_LEDGER_19C.DEPARTMENTS (
    DEPT_ID NUMBER PRIMARY KEY,
    DEPT_NAME VARCHAR2(100)
);

-- 4. Expense Categories Table
CREATE TABLE RB_INTEL_LEDGER_19C.EXPENSE_CATEGORIES (
    CATEGORY_ID NUMBER PRIMARY KEY,
    CATEGORY_NAME VARCHAR2(100),
    BUDGET_LIMIT NUMBER(12,2)
);

-- 5. Employees Table
CREATE TABLE RB_INTEL_LEDGER_19C.EMPLOYEES (
    EMP_ID NUMBER PRIMARY KEY,
    FIRST_NAME VARCHAR2(50),
    LAST_NAME VARCHAR2(50),
    DEPT_ID NUMBER REFERENCES RB_INTEL_LEDGER_19C.DEPARTMENTS(DEPT_ID),
    COUNTRY_ID NUMBER REFERENCES RB_INTEL_LEDGER_19C.COUNTRIES(COUNTRY_ID)
);

-- 6. Expenses Table
CREATE TABLE RB_INTEL_LEDGER_19C.EXPENSES (
    EXPENSE_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    AMOUNT NUMBER(12,2),
    EXPENSE_DATE TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    MERCHANT_NAME VARCHAR2(100),
    DESCRIPTION VARCHAR2(255),
    STATUS VARCHAR2(50),
    EMP_ID NUMBER REFERENCES RB_INTEL_LEDGER_19C.EMPLOYEES(EMP_ID),
    CATEGORY_ID NUMBER REFERENCES RB_INTEL_LEDGER_19C.EXPENSE_CATEGORIES(CATEGORY_ID)
);

-- Create global public synonym for local backend Python interface query compatibility
CREATE OR REPLACE PUBLIC SYNONYM EXPENSES FOR RB_INTEL_LEDGER_19C.EXPENSES;

-- Grant select and update permissions directly to common database interface user to allow AI status holds/rejections
GRANT SELECT, UPDATE ON RB_INTEL_LEDGER_19C.EXPENSES TO c##datastream;

