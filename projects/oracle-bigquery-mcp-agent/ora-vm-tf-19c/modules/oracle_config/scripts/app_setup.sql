    v_date TIMESTAMP;
    v_amount NUMBER(12,2);
    v_status VARCHAR2(20);
    v_merchant VARCHAR2(100);
    v_desc VARCHAR2(255);
    v_emp_id NUMBER;
    v_cat_id NUMBER;
    v_country_id NUMBER;
    v_dept_id NUMBER;
    TYPE t_str_arr IS TABLE OF VARCHAR2(100);
    v_merchants t_str_arr := t_str_arr('Google Cloud Platform', 'Apple Store', 'Uber Enterprise', 'Hilton Hotels', 'British Airways', 'Cisco Systems', 'Amazon Web Services', 'Salesforce.com', 'Microsoft Azure', 'WeWork Enterprise');
    v_descs t_str_arr := t_str_arr('Enterprise cloud compute cluster expansion', 'Urgent developer hardware provisioning', 'Client site transfer coordination', 'Global developer summit accommodations', 'Quarterly software licensing renewals', 'Network security appliance hardware upgrade', 'Database storage high-water mark allocation', 'Executive client dinner logistics', 'Urgent SRE emergency hardware replacement', 'Annual SaaS contract commitment');
BEGIN
    DBMS_OUTPUT.PUT_LINE('Seeding baseline categorical metadata...');
    INSERT INTO RB_INTEL_LEDGER_19C.REGIONS (REGION_ID, REGION_NAME, DESCRIPTION) VALUES (1, 'AMER', 'Americas Enterprise Operations');
    INSERT INTO RB_INTEL_LEDGER_19C.REGIONS (REGION_ID, REGION_NAME, DESCRIPTION) VALUES (2, 'EMEA', 'Europe, Middle East & Africa');
