    INSERT INTO RB_INTEL_LEDGER_19C.COUNTRIES (COUNTRY_ID, COUNTRY_NAME, REGION_ID) VALUES (101, 'United States', 1);
    INSERT INTO RB_INTEL_LEDGER_19C.COUNTRIES (COUNTRY_ID, COUNTRY_NAME, REGION_ID) VALUES (102, 'United Kingdom', 2);
    INSERT INTO RB_INTEL_LEDGER_19C.COUNTRIES (COUNTRY_ID, COUNTRY_NAME, REGION_ID) VALUES (103, 'Japan', 3);

    INSERT INTO RB_INTEL_LEDGER_19C.DEPARTMENTS (DEPT_ID, DEPT_NAME) VALUES (10, 'Cloud Platform Infrastructure');
    INSERT INTO RB_INTEL_LEDGER_19C.DEPARTMENTS (DEPT_ID, DEPT_NAME) VALUES (20, 'Enterprise Security Engineering');
    INSERT INTO RB_INTEL_LEDGER_19C.DEPARTMENTS (DEPT_ID, DEPT_NAME) VALUES (30, 'Global Financial Operations');

    INSERT INTO RB_INTEL_LEDGER_19C.EXPENSE_CATEGORIES (CATEGORY_ID, CATEGORY_NAME, BUDGET_LIMIT) VALUES (1, 'Cloud Hosting SLA', 5000000.00);
    INSERT INTO RB_INTEL_LEDGER_19C.EXPENSE_CATEGORIES (CATEGORY_ID, CATEGORY_NAME, BUDGET_LIMIT) VALUES (2, 'Hardware Provisioning', 250000.00);
    INSERT INTO RB_INTEL_LEDGER_19C.EXPENSE_CATEGORIES (CATEGORY_ID, CATEGORY_NAME, BUDGET_LIMIT) VALUES (3, 'Travel & Entertainment', 100000.00);

    DBMS_OUTPUT.PUT_LINE('Generating 2,000 enterprise employee records...');
    FOR i IN 1..2000 LOOP
        v_dept_id := CASE MOD(i, 3) WHEN 0 THEN 10 WHEN 1 THEN 20 ELSE 30 END;
        v_country_id := CASE MOD(i, 3) WHEN 0 THEN 101 WHEN 1 THEN 102 ELSE 103 END;
        INSERT INTO RB_INTEL_LEDGER_19C.EMPLOYEES (EMP_ID, FIRST_NAME, LAST_NAME, DEPT_ID, COUNTRY_ID)
        VALUES (1000 + i, 'EmpFirst_' || i, 'EmpLast_' || i, v_dept_id, v_country_id);
    END LOOP;

    DBMS_OUTPUT.PUT_LINE('Generating 50,000 continuous expense records across 730 days of historical spend...');
    FOR i IN 1..50000 LOOP
        v_emp_id := 1001 + ABS(MOD(DBMS_RANDOM.RANDOM, 2000));
        v_cat_id := 1 + ABS(MOD(DBMS_RANDOM.RANDOM, 3));
        v_merchant := v_merchants(1 + ABS(MOD(DBMS_RANDOM.RANDOM, 10)));
        v_desc := v_descs(1 + ABS(MOD(DBMS_RANDOM.RANDOM, 10)));
        v_amount := ROUND(DBMS_RANDOM.VALUE(100.00, 25000.00), 2);
        v_date := SYSTIMESTAMP - INTERVAL '1' DAY * ROUND(DBMS_RANDOM.VALUE(0, 730)) - INTERVAL '1' MINUTE * ROUND(DBMS_RANDOM.VALUE(0, 1440));

        -- Ensure high-risk items are distinctly seeded in pending queues
        IF MOD(i, 100) = 0 THEN
            v_status := 'PENDING';
            v_amount := ROUND(DBMS_RANDOM.VALUE(15500.00, 45000.00), 2);
            v_desc := 'Urgent MacBook Pro M3 Max hardware replacement for personal use';
        ELSIF MOD(i, 50) = 0 THEN
            v_status := 'HOLD';
        ELSE
            v_status := 'APPROVED';
        END IF;

        INSERT INTO RB_INTEL_LEDGER_19C.EXPENSES (AMOUNT, EXPENSE_DATE, MERCHANT_NAME, DESCRIPTION, STATUS, EMP_ID, CATEGORY_ID)
        VALUES (v_amount, v_date, v_merchant, v_desc, v_status, v_emp_id, v_cat_id);

        IF MOD(i, 5000) = 0 THEN
            COMMIT;
            DBMS_OUTPUT.PUT_LINE('Committed ' || i || ' expense records...');
        END IF;
    END LOOP;
    COMMIT;
    DBMS_OUTPUT.PUT_LINE('Successfully generated all 50,000 records!');
